backslash_cat = "I'm \\ a \\ cat."#sets variable backslash_cat equal to "I'm \\ a \\ cat." \\ creates a backslash

fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
''' #sets fat_cat variable equal to the contents of the string \t* tabs in

print(tabby_cat) # prints tabby_cat
